        public static AddressDto GetAddress(int personId, AddressType? addressType = AddressType.Mailing)
        {
            string isoCountryCode = ISOCountryCodes[Random.Next() % ISOCountryCodes.Count];
            int addressTypeInt = addressType != null ? (int)addressType.Value : Random.Next() % 100;
            addressTypeInt = addressTypeInt < 1 ? addressTypeInt : 99;

            AddressDto entity = new AddressDto
            {
                PersonId = personId,
                StreetAddress1 = $"{(Random.Next() % 99999) + 1} {string.Join(" ", IpsumGen.GenerateWords((Random.Next() % 3) + 1))} {StreetSuffixes[Random.Next() % StreetSuffixes.Count]}",
                City = string.Join(" ", IpsumGen.GenerateWords((Random.Next() % 5) + 1)),
                ISOCountryCode = isoCountryCode,
                ISOCountrySubdivisionLevel2Code = $"{isoCountryCode}-{Convert.ToString(Random.Next() % 100).PadLeft(2, '0')}",
                PostalCode = $"{Convert.ToString((Random.Next() % 99999) + 1).PadLeft(5, '0')}",
                AddressType = (AddressType)addressTypeInt
            };
            return entity;
        }
